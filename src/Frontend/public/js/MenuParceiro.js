function changePage(index){
    switch (index){
        case 0:
            window.location = 'SolicitaçãoAntecipação.html';
            break;
        case 1:
            window.location = 'HistoricoSolicitacao.html';
            break;
        case 2:
            window.location = 'EditarPerfil.html';
            break;
    }
}